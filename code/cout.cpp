#include<bits/stdc++.h>

using namespace std;

int main()
{
	// cout is used for outputs
	cout << 1 << endl;	

	// cout can be used with std:: , if we are using different namespaces

	std::cout << 1 << std::endl;

	// cout can output various variables

	int c = 25;

	cout << c << endl;
	std::cout << c << std::endl;

	return 0;
}